function updatePricingButtons() {
    document.querySelectorAll('.pricing-card').forEach(card => {
        const plan = card.dataset.plan;
        const btn = card.querySelector('button');
        btn.disabled = false;
        btn.classList.remove('btn-disabled');
        if (currentTier === null) {
            if (plan === 'basic') {
                btn.textContent = translations[currentLanguage].get_started;
                btn.onclick = () => startNow();
            } else {
                btn.textContent = translations[currentLanguage].choose_plan;
                btn.onclick = () => subscribe(plan);
            }
        } else {
            if (plan === currentTier) {
                btn.textContent = translations[currentLanguage].current_plan || 'Current Plan';
                btn.disabled = true;
                btn.classList.add('btn-disabled');
            } else if (plan === 'basic' && currentTier !== 'basic') {
                btn.textContent = translations[currentLanguage].downgrade_to_basic || 'Downgrade to Basic';
                btn.onclick = cancelSubscription;
            } else {
                btn.textContent = currentTier === 'basic' ? 
                    (translations[currentLanguage].upgrade_to || 'Upgrade to ') + plan.charAt(0).toUpperCase() + plan.slice(1) : 
                    (translations[currentLanguage].switch_to || 'Switch to ') + plan.charAt(0).toUpperCase() + plan.slice(1);
                btn.onclick = () => subscribe(plan);
            }
        }
    });
}

