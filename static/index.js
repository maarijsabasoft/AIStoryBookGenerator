function setLanguage(lang) {
    currentLanguage = lang;
    document.documentElement.lang = lang;

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Update placeholder attributes
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });

    // Update language select dropdown to reflect current language
    const languageSelect = document.getElementById('language');
    if (languageSelect) {
        for (let i = 0; i < languageSelect.options.length; i++) {
            const optionText = translations[lang][languageSelect.options[i].getAttribute('data-i18n')];
            if (optionText) {
                languageSelect.options[i].textContent = optionText;
            }
        }
        languageSelect.value = translations[lang][lang] || lang;
    }

    // Update orientation select dropdown
    const orientationSelect = document.getElementById('orientation');
    if (orientationSelect) {
        for (let i = 0; i < orientationSelect.options.length; i++) {
            const optionText = translations[lang][orientationSelect.options[i].getAttribute('data-i18n')];
            if (optionText) {
                orientationSelect.options[i].textContent = optionText;
            }
        }
        orientationSelect.value = translations[lang][orientationSelect.value] || orientationSelect.value;
    }

    // Update length select options
    updateLengthOptions();

    // Close dropdown after selection
    document.getElementById('translationDropdown').classList.remove('active');
}

