async function downloadPDF() {
const { jsPDF } = window.jspdf;

// Hardcoded to A4 portrait
const doc = new jsPDF({
orientation: 'portrait',
unit: 'px',
format: [794, 1123] // A4 portrait size in px
});

const pages = document.querySelectorAll('.story-page');
let currentPage = 0;

for (const page of pages) {
const canvas = await html2canvas(page, {
    scale: 2,
    useCORS: true,
    backgroundColor: null // keeps preview colors exactly as shown
});

const imgData = canvas.toDataURL('image/jpeg', 1.0);
const imgProps = doc.getImageProperties(imgData);
const pdfWidth = doc.internal.pageSize.getWidth();
const imgHeight = (imgProps.height * pdfWidth) / imgProps.width;

if (currentPage > 0) {
    doc.addPage();
}

doc.addImage(imgData, 'JPEG', 0, 0, pdfWidth, imgHeight);
currentPage++;
}

doc.save('PromptToStory_Story.pdf');
}


