let currentOtpEmail = '';

function handleAuth() {
    const isSignIn = document.getElementById('modalTitle').textContent === translations[currentLanguage].sign_in;
    const email = document.getElementById('email').value;
    const password = document.getElementById('password').value;
    const name = document.getElementById('name').value;
    const errorMessage = document.getElementById('errorMessage');

    if (isSignIn) {
        // For sign in, proceed normally (no OTP)
        fetch('/api/login', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ email, password })
        })
        .then(res => res.json())
        .then(data => {
            if (data.error) {
                errorMessage.textContent = data.error;
                errorMessage.style.display = 'block';
            } else {
                closeAuthModal();
                updateUserUI(data.user);
                updatePricingButtons();
                displayFlashMessages();
                // Redirect to home page
                window.location.href = '/';
            }
        })
        .catch(err => {
            errorMessage.textContent = translations[currentLanguage].error_occurred || 'An error occurred';
            errorMessage.style.display = 'block';
        });
    } else {
        // For sign up, send OTP
        fetch('/api/signup', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ name, email, password })
        })
        .then(res => res.json())
        .then(data => {
            if (data.error) {
                errorMessage.textContent = data.error;
                errorMessage.style.display = 'block';
            } else {
                // Store email for verification
                currentOtpEmail = email;
                
                // Close auth modal and show OTP modal
                closeAuthModal();
                openOtpModal(email);
            }
        })
        .catch(err => {
            errorMessage.textContent = translations[currentLanguage].error_occurred || 'An error occurred';
            errorMessage.style.display = 'block';
        });
    }
}

