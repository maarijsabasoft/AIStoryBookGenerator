function createAnimalCharacters() {
    const sections = [
        document.getElementById('hero'),
        document.getElementById('features'),
        document.getElementById('how-it-works'),
        document.getElementById('examples'),
        document.getElementById('pricing')
    ];

    const animations = ['float', 'float2', 'float3', 'bounce'];

    sections.forEach(section => {
        if (!section) return;

        const numAnimals = Math.floor(Math.random() * 2) + 6;
        const positions = [];
        const rows = 4;
        const cols = 4;

        for (let row = 0; row < rows; row++) {
            for (let col = 0; col < cols; col++) {
                positions.push({
                    top: (row * 25) + 5,
                    left: (col * 25) + 5
                });
            }
        }

        shuffleArray(positions);

        for (let i = 0; i < numAnimals; i++) {
            const animal = document.createElement('div');
            animal.className = 'animal-character';
            const animalNum = Math.floor(Math.random() * 19) + 1;
            animal.style.backgroundImage = `url('/static/${animalNum}.png')`;
            const position = positions[i % positions.length];
            animal.style.top = `${position.top}%`;
            animal.style.left = `${position.left}%`;
            const size = Math.floor(Math.random() * 20) + 100;
            animal.style.width = `${size}px`;
            animal.style.height = `${size}px`;
            const animation = animations[Math.floor(Math.random() * animations.length)];
            const duration = Math.floor(Math.random() * 6) + 4;
            const delay = Math.floor(Math.random() * 5);
            animal.style.animation = `${animation} ${duration}s infinite ease-in-out ${delay}s`;
            section.appendChild(animal);
        }
    });
}

function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

